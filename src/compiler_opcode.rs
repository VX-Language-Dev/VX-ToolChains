// ==================== 编译器 OpCode ====================
// 编译器内部的指令集定义（与 VM 的 OpCode 值一致但独立维护）

#[repr(u8)]
#[derive(Debug, Clone, Copy, PartialEq)]
#[allow(dead_code)]
pub enum OpCode {
    LoadConst = 0x01,
    LoadNil = 0x02,
    LoadTrue = 0x03,
    LoadFalse = 0x04,
    LoadVar = 0x05,
    StoreVar = 0x06,
    DefineVar = 0x07,
    Call = 0x08,
    Return = 0x09,
    MakeFunction = 0x0a,
    Jump = 0x0b,
    JumpIfFalse = 0x0c,
    JumpIfTrue = 0x0d,
    Break = 0x0e,
    Continue = 0x0f,
    BinaryAdd = 0x10,
    BinarySub = 0x11,
    BinaryMul = 0x12,
    BinaryDiv = 0x13,
    BinaryMod = 0x14,
    BinaryPow = 0x15,
    BinaryEq = 0x16,
    BinaryNe = 0x17,
    BinaryLt = 0x18,
    BinaryGt = 0x19,
    BinaryLe = 0x1a,
    BinaryGe = 0x1b,
    BinaryAnd = 0x1c,
    BinaryOr = 0x1d,
    UnaryNeg = 0x1e,
    UnaryNot = 0x1f,
    MakeStruct = 0x20,
    MakeClass = 0x21,
    MakeEnum = 0x22,
    MakeUnion = 0x23,
    LoadField = 0x24,
    StoreField = 0x25,
    MakeArray = 0x26,
    MakeMap = 0x27,
    IndexGet = 0x28,
    IndexSet = 0x29,
    PropertyGet = 0x2a,
    PropertySet = 0x2b,
    AddressOf = 0x2c,
    Deref = 0x2d,
    PointerMember = 0x2e,
    Import = 0x2f,
    New = 0x30,
    Halt = 0x31,
    SysArgv = 0x32,
    System = 0x33,
    FileRead = 0x34,
    FileWrite = 0x35,
    FileExists = 0x36,
    Dup = 0x37,
    Pop = 0x38,
    Newz = 0x39,
    Free = 0x3a,
    OwnershipMove = 0x3b,
    ScopeDrop = 0x3c,
    BorrowCheck = 0x3d,
    AliveCheck = 0x3e,
}
